
/**
 * sum - stores the  sum of the two parameters
 * @arg1:first operand
 * @arg2:second operand
 *
 * Return: Sum
 */
#include <stdio.h>

int main(int arg1, int arg2)
{
        int sum;

        sum = arg1 + arg2;
        printf("The sum of the two numbers is: %i\n", sum);

        return (sum);
}
